// public/scripts/profile-check.js
// Shared profile completeness checker for Hemline Market
// Include this script on pages that require a complete profile (sell, checkout, etc.)
//
// Source-of-truth fix:
//   account.html saves the seller's name + ship-from address into
//   auth.users.user_metadata (keys: ship_name, ship_address1, ship_city,
//   ship_state, ship_postal). The Stripe webhook also reads from there
//   when creating Shippo labels. The previous version of this file read
//   `address_line1`/`address_city`/etc. from the `profiles` table — fields
//   the Account page never writes to — so completed sellers were being
//   blocked from listing. Now we read from user_metadata first and only
//   fall back to the profiles table for legacy data.

window.HM = window.HM || {};

// Check if this is a test/fake account that should bypass profile completion
window.HM.isTestAccount = function(email) {
  if (!email) return false;
  const testPatterns = [
    'hemline.seller',
    'hemline.buyer',
    'hemline.test',
    'test@hemline',
    '@hemlinemarket.com'
  ];
  const lowerEmail = email.toLowerCase();
  return testPatterns.some(pattern => lowerEmail.includes(pattern));
};

window.HM.checkProfileComplete = function() {
  try {
    const raw = localStorage.getItem('hm_profile_complete');
    if (!raw) return { complete: false, hasRealName: false, hasAddress: false };
    return JSON.parse(raw);
  } catch (e) {
    return { complete: false, hasRealName: false, hasAddress: false };
  }
};

// Async version that verifies from Supabase if localStorage says incomplete
window.HM.checkProfileCompleteAsync = async function() {
  // First check localStorage
  const localStatus = window.HM.checkProfileComplete();
  if (localStatus.complete) return localStatus;

  // If localStorage says incomplete, verify from Supabase
  try {
    const supabase = window.HM?.supabase;
    if (!supabase) return localStatus;

    const { data: { session } } = await supabase.auth.getSession();
    if (!session?.user) return localStatus;

    const userId = session.user.id;
    const userEmail = session.user.email || '';
    const userMeta = session.user.user_metadata || {};

    // Test accounts bypass profile completion requirements
    if (window.HM.isTestAccount(userEmail)) {
      const testStatus = { complete: true, hasRealName: true, hasAddress: true, hasContactEmail: true, userId, isTestAccount: true };
      localStorage.setItem('hm_profile_complete', JSON.stringify(testStatus));
      console.log('[profile-check] Test account bypass:', userEmail);
      return testStatus;
    }

    // Helper: detect auto-generated names so they don't pass as "real names"
    const isAutoGeneratedName = (name) => {
      if (!name) return true;
      const looksAutoGenerated = /^[a-z0-9]{6,}$/.test(name) && /\d/.test(name);
      return looksAutoGenerated || name === "Hemline member";
    };

    const isRelayEmail = (email) => {
      if (!email) return false;
      return email.includes('@relay.') || email.includes('.relay@');
    };

    // ---------- Pull profile row (legacy fallback for first/last name) ----------
    const { data: profileData } = await supabase
      .from('profiles')
      .select('first_name, last_name, contact_email')
      .eq('id', userId)
      .maybeSingle();

    const profile = profileData || {};

    // ---------- Name check ----------
    // Prefer profiles.first_name/last_name, fall back to user_metadata.ship_name
    // (which the Account page writes when the seller fills in their ship-from
    // address — it's their real name as it should appear on shipping labels).
    let firstName = profile.first_name || '';
    let lastName = profile.last_name || '';
    if ((!firstName || !lastName) && userMeta.ship_name) {
      const parts = String(userMeta.ship_name).trim().split(/\s+/);
      if (!firstName) firstName = parts[0] || '';
      if (!lastName && parts.length > 1) lastName = parts.slice(1).join(' ');
    }

    const hasRealName = !!(firstName && lastName &&
                          !isAutoGeneratedName(firstName) &&
                          !isAutoGeneratedName(lastName));

    // ---------- Address check ----------
    // The Account page's "Ship-from address" form saves to user_metadata.
    // The Stripe webhook reads ship_address1/ship_city/ship_state/ship_postal
    // from there to create Shippo labels — so that's the canonical location.
    const hasAddress = !!(
      userMeta.ship_address1 &&
      userMeta.ship_city &&
      userMeta.ship_state &&
      userMeta.ship_postal
    );

    // ---------- Contact email check ----------
    // contact_email is optional; if missing, fall back to the auth email
    // (as long as it isn't an Apple/Google relay address, which can bounce).
    let contactEmail = profile.contact_email || '';
    if (!contactEmail && userEmail && !isRelayEmail(userEmail)) {
      contactEmail = userEmail;
    }
    const hasContactEmail = !!(contactEmail && !isRelayEmail(contactEmail));

    const complete = hasRealName && hasAddress && hasContactEmail;

    // Update localStorage with fresh data
    const freshStatus = { complete, hasRealName, hasAddress, hasContactEmail, userId };
    localStorage.setItem('hm_profile_complete', JSON.stringify(freshStatus));

    console.log('[profile-check] Verified from Supabase:', freshStatus);
    return freshStatus;
  } catch (e) {
    console.warn('[profile-check] Supabase check failed:', e);
    return localStatus;
  }
};

window.HM.requireCompleteProfile = function(options = {}) {
  const {
    redirectTo = 'account.html',
    message = 'Please complete your profile before continuing.',
    allowContinue = false
  } = options;

  const status = window.HM.checkProfileComplete();

  if (!status.complete) {
    const missing = [];
    if (!status.hasRealName) missing.push('your name');
    if (!status.hasAddress) missing.push('your shipping address');

    const fullMessage = message + '\n\nMissing: ' + missing.join(' and ');

    if (allowContinue) {
      if (!confirm(fullMessage + '\n\nGo to your account to complete your profile?')) {
        return status; // User chose to continue anyway
      }
    } else {
      alert(fullMessage);
    }

    window.location.href = redirectTo;
    return null; // Will redirect
  }

  return status;
};

// Show a warning banner on the current page (async version that verifies from DB)
window.HM.showProfileWarningBanner = async function(containerId, options = {}) {
  const status = await window.HM.checkProfileCompleteAsync();
  if (status.complete) return;

  const container = document.getElementById(containerId);
  if (!container) return;

  const missing = [];
  if (!status.hasRealName) missing.push('your name');
  if (!status.hasAddress) missing.push('a shipping address');

  const message = options.message || 'Complete your profile before listing items.';
  const detail = 'Please add ' + missing.join(' and ') + '.';

  container.innerHTML = `
    <div style="background:#fff7d6;border:1px solid #f0d97a;border-radius:12px;padding:14px 18px;margin:14px 0;display:flex;align-items:center;justify-content:space-between;gap:14px;flex-wrap:wrap;">
      <div style="display:flex;align-items:center;gap:10px;">
        <span style="font-size:20px;">⚠️</span>
        <div>
          <strong style="color:#7a5d12;">${message}</strong>
          <div style="font-size:13px;color:#7a5d12;margin-top:2px;">${detail}</div>
        </div>
      </div>
      <a href="account.html" style="background:#7a1f1f;color:#fff;padding:8px 16px;border-radius:8px;text-decoration:none;font-size:14px;font-weight:600;white-space:nowrap;">Complete Profile</a>
    </div>
  `;
};

// Clear the cached completion status — call this from the Account page after
// the seller saves their profile so the next page-load reflects the change
// without forcing a full sign-out / sign-in cycle.
window.HM.invalidateProfileCheckCache = function() {
  try { localStorage.removeItem('hm_profile_complete'); } catch (e) {}
};
